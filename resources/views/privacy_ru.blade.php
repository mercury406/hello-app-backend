    Мы храним ваши актуальные контакты, чтобы уведомить вас, как только один из ваших контактов зарегистрируется на <strong>MetaHi!</strong> и правильно отображать имена в уведомлениях. Нам нужны только номер и имя (имя и фамилия), чтобы это работало и не сохраняло никаких других данных о ваших контактах.<br /><br />

    Вы всегда можете прекратить синхронизацию контактов или удалить их с наших серверов и перестать использовать наш <strong>MetaHi!</strong> щелкнув действие &laquo;Удалить данные и выйти&raquo; в меню приложения.<br /><strong>2.2. Данные о местонахождении</strong><br /><br />

