    Когда дело доходит до сбора и обработки личных данных, у <strong>MetaHi!</strong> есть фундаментальные принципы:<br /><br />

    &nbsp;&nbsp;&nbsp; Мы не используем ваши данные для показа вам рекламы.<br />&nbsp;&nbsp;&nbsp; Мы храним только те данные, которые <strong>MetaHi!</strong> необходимо функционировать.<br /><br />

    Функция <strong>MetaHi!</strong> предназначена для отправки HighFives зарегистрированным в приложении контактам пользователя, которые находятся на расстоянии не более 25 метров, а последнее время отправки местоположения не более 1 часа.<br />
